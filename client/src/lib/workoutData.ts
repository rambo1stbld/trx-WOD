export interface Exercise {
  id: string;
  name: string;
  focus: string;
  duration: number; // seconds
  animation: AnimationKeyframe[];
}

export interface AnimationKeyframe {
  label: string;
  // Body parts positions (percentage-based for SVG)
  head: { x: number; y: number };
  torso: { x1: number; y1: number; x2: number; y2: number };
  leftArm: { x1: number; y1: number; x2: number; y2: number; x3: number; y3: number };
  rightArm: { x1: number; y1: number; x2: number; y2: number; x3: number; y3: number };
  leftLeg: { x1: number; y1: number; x2: number; y2: number; x3: number; y3: number };
  rightLeg: { x1: number; y1: number; x2: number; y2: number; x3: number; y3: number };
  straps?: { x1: number; y1: number; x2: number; y2: number };
  straps2?: { x1: number; y1: number; x2: number; y2: number };
}

export interface WorkoutPhase {
  name: string;
  duration: string;
  exercises: Exercise[];
  workTime: number;
  restTime: number;
  rounds: number;
}

